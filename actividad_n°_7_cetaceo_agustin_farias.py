#------------------MAMIFEROS--------------------
class Mamifero:
    def __init__(self, cantMamas, esperanzaDeVida):
        self.esperanzaDeVida = esperanzaDeVida
        self.cantMamas = cantMamas
    
    def mamar(self):
        print("Puede amamantar")
    
    def nacer(self):
        print("Nacimiento")

#------------------ANIMALES MARINOS--------------------

class AnimalMarino:
    def __init__(self, tieneBranqueas, especie):
        self.tieneBranqueas = tieneBranqueas
        self.especie = especie
    
    def nadar(self):
        print("Puede nadar")

#------------------CETACEOS--------------------

class Cetaceo(Mamifero,AnimalMarino):
    def __init__( self, notas, viveEn, peso, esperanzaDeVida,cantMamas,tieneBranqueas, especie):
      
      Mamifero.__init__(self,esperanzaDeVida, cantMamas)
      AnimalMarino.__init__(self,tieneBranqueas, especie)

      self.notas = notas
      self.viveEn = viveEn
      self.peso = peso

    def nacer(self):
        print("Nacimiento")

